let n1 = -100;

if (n1 > 0) {
  console.log(`${n1} é um número positivo `);
} else if (n1 === 0) {
  console.log(`${n1} é um número nulo`);
} else if (n1 < 0) {
  console.log(`${n1} é um número negativo`);
}
